package JavaBasic;

public class Rectangle {

    public static void main(String[] args) {

        int length = 20;
        int breadth = 15;

        int area;
        int perimeter;

        area = length * breadth ;  // 300

        perimeter = 2*(length + breadth); // 70

        System.out.println("Area="+area);
        System.out.println("Perimeter="+perimeter);

    }
}
