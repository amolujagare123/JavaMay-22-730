package CollectionsDemos.SetDemo;

import java.util.HashSet;

public class HashSetDemo {

    public static void main(String[] args) {


        HashSet hs = new HashSet();

        hs.add(1);
        hs.add('g');
        hs.add(true);
        hs.add("amol");
        hs.add(11.33);

        System.out.println(hs);

    }
}
