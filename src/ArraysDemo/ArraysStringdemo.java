package ArraysDemo;

public class ArraysStringdemo {

    public static void main(String[] args) {

        String[] a = {"divya","jyoti","madhavi","pallavi","priyanka"};

        System.out.println("size="+a.length);

        for (int i=0;i<a.length;i++)
        {
            System.out.println(a[i]);
        }

    }
}
