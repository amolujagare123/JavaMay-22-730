package ArraysDemo;

public class ArraysDoubledemo {

    public static void main(String[] args) {

        double[] a = {1.2,13.1,4.5,2.11,41.1,1.2,1.0};

        System.out.println("size="+a.length);

        for (int i=0;i<a.length;i++)
        {
            System.out.println(a[i]);
        }

    }
}
